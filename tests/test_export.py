# ---------------------------------------------------------------------------
# Vivaldi -- shortcuts (no defaults mirror, so emits all non-empty bindings)
# ---------------------------------------------------------------------------

def test_vivaldi_export_emits_non_empty_bindings(
    fake_vivaldi_profile_root: Path,
    tmp_path: Path,
    capsys: pytest.CaptureFixture,
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    """Vivaldi has no defaults mirror.  Its export emits every command
    with a non-empty `shortcuts` field -- the closest approximation
    available -- and the export header documents that limitation."""
    fake_policy = tmp_path / "policy" / "dotbrowser-pwa.json"
    _redirect_pwa(monkeypatch, vivaldi_pwa, fake_policy)

    args = argparse.Namespace(
        profile_root=fake_vivaldi_profile_root,
        profile="Default",
        output=None,
        all_shortcuts=False,
    )
    vivaldi_pkg.cmd_export(args)
    out = capsys.readouterr().out

    doc = tomllib.loads(out)
    assert set(doc["shortcuts"]) == {
        "COMMAND_CLOSE_TAB",
        "COMMAND_NEW_TAB",
        "COMMAND_FOCUS_ADDRESSFIELD",
        "COMMAND_TAB_NEXT",
    }
    # PWA section emits even when empty (so the user sees the table exists).
    assert doc["pwa"]["urls"] == []


# ---------------------------------------------------------------------------
# CLI wiring smoke -- export subparser is registered
# ---------------------------------------------------------------------------

@pytest.mark.parametrize("browser", ["brave", "vivaldi", "edge", "chrome"])
def test_export_subcommand_is_registered(browser: str) -> None:
    """`<browser> export --help` succeeds for every supported browser."""
    from dotbrowser.cli import build_parser
    parser = build_parser()
    # parse_args raises SystemExit on --help, so just check the parser
    # has an `export` action wired up.
    sub_actions = [
        a for a in parser._actions
        if isinstance(a, argparse._SubParsersAction)
    ]
    assert len(sub_actions) == 1
    browser_choices = sub_actions[0].choices
    assert browser in browser_choices
    browser_parser = browser_choices[browser]
    actions = [
        a for a in browser_parser._actions
        if isinstance(a, argparse._SubParsersAction)
    ]
    assert len(actions) == 1
    assert "export" in actions[0].choices
