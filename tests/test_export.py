def _make_minimal_profile(tmp_path: Path) -> Path:
    """Create a Default profile with a minimal Preferences for browsers
    that don't need shortcut/setting fixtures (Edge/Chrome export)."""
    profile = tmp_path / "Default"
    profile.mkdir()
    (profile / "Preferences").write_text(json.dumps({"some": "thing"}))
    return tmp_path


def test_edge_export_omits_shortcuts(
    tmp_path: Path,
    capsys: pytest.CaptureFixture,
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    profile_root = _make_minimal_profile(tmp_path)
    fake_policy = tmp_path / "policy" / "dotbrowser-pwa.json"
    _redirect_pwa(monkeypatch, edge_pwa, fake_policy)
    _seed_policy(fake_policy, ["https://app.element.io/"])

    args = argparse.Namespace(
        profile_root=profile_root,
        profile="Default",
        output=None,
    )
    edge_pkg.cmd_export(args)
    out = capsys.readouterr().out

    doc = tomllib.loads(out)
    # The header comment legitimately mentions `[shortcuts]` while
    # explaining what the export covers; the TOML body must not have
    # an actual shortcuts table.
    assert "shortcuts" not in doc
    assert doc["pwa"]["urls"] == ["https://app.element.io/"]


